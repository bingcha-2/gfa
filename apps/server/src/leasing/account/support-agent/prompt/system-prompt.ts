/** 一条知识。 */
export interface KnowledgeQA {
  question: string;
  answer: string;
  category?: string | null;
}

/**
 * 「全塞」模式:把全部已发布知识拼到系统提示词末尾。
 * 知识库小的场景下省掉检索往返,直接让模型据此作答。
 */
export function buildSystemPrompt(entries: KnowledgeQA[]): string {
  if (entries.length === 0) {
    return `${SUPPORT_SYSTEM_PROMPT}\n\n## 知识库\n(暂无知识条目。产品使用类问题如无法确定,请创建工单转人工。)`;
  }
  const kb = entries
    .map((e, i) => {
      const cat = e.category ? `[${e.category}] ` : "";
      return `${i + 1}. ${cat}问:${e.question}\n   答:${e.answer}`;
    })
    .join("\n\n");
  return `${SUPPORT_SYSTEM_PROMPT}\n\n## 知识库(据此回答产品使用类问题;无对应内容则转人工)\n${kb}`;
}
